// Helper functions to get colors with fallback
export const getIncomeCategoryColor = (category: string) => {
  return incomeCategoryColors[category] || incomeCategoryColors["Other"];
};

export const getExpenseCategoryColor = (category: string) => {
  return expenseCategoryColors[category] || expenseCategoryColors["Other"];
};

// Get just the border color part for cards
export const getIncomeCategoryBorderColor = (category: string) => {
  const fullColor = getIncomeCategoryColor(category);
  const borderColor = fullColor.split(" ").find((c) => c.startsWith("border-"));
  return borderColor || "border-slate-400";
};

export const getExpenseCategoryBorderColor = (category: string) => {
  const fullColor = getExpenseCategoryColor(category);
  const borderColor = fullColor.split(" ").find((c) => c.startsWith("border-"));
  return borderColor || "border-slate-400";
};

// Get just the background color for icons
export const getIncomeCategoryBgColor = (category: string) => {
  const fullColor = getIncomeCategoryColor(category);
  const bgColor = fullColor
    .split(" ")
    .find(
      (c) =>
        c.startsWith("bg-") && !c.includes("hover:") && !c.includes("dark:")
    );
  return bgColor || "bg-slate-50";
};

